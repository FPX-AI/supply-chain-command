// ─── HELPERS ────────────────────────────────────────────────────────────────
export const pct = (s, n) => (((n - s) / s) * 100).toFixed(1);

export const getAllCompanies = (report) => {
  const stages = report.chips
    ? report.chips.flatMap((c) => c.stages)
    : report.stages;
  const all = stages.flatMap((s) =>
    s.companies.map((c) => ({
      ...c,
      stageColor: report.color,
      stageName: s.name,
      unlocked: report.unlocked,
    }))
  );
  // Deduplicate by ticker (chips may share companies)
  const seen = new Set();
  return all.filter((c) => {
    if (seen.has(c.ticker)) return false;
    seen.add(c.ticker);
    return true;
  });
};

export const getAllCompaniesAcrossReports = () =>
  REPORTS.flatMap((r) => getAllCompanies(r));

// ─── AUTO-UPDATE PRICES ──────────────────────────────────────────────────────
// Fetches /prices.json (generated daily by update-prices.js) and patches all
// company.now values in place so the dashboard always shows current prices.
export const updatePricesFromJSON = async () => {
  try {
    const resp = await fetch("/prices.json?t=" + Date.now());
    if (!resp.ok) return false;
    const prices = await resp.json();
    if (!prices || !prices.updated) return false;
    for (const report of REPORTS) {
      const allStages = report.chips
        ? report.chips.flatMap((c) => c.stages)
        : report.stages;
      for (const stage of allStages) {
        for (const company of stage.companies) {
          if (prices[company.ticker] != null) {
            company.now = prices[company.ticker];
          }
        }
      }
    }
    return prices.updated;
  } catch {
    return false;
  }
};
