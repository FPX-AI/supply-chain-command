// ─── HELPERS ────────────────────────────────────────────────────────────────
export const pct = (s, n) => s === 0 ? "0.0" : (((n - s) / s) * 100).toFixed(1);

export const getAllCompanies = (report) => {
  const stages = report.chips
    ? report.chips.flatMap((c) => c.stages)
    : report.stages;
  const all = stages.flatMap((s) =>
    s.companies.map((c) => ({
      ...c,
      stageColor: report.color,
      stageName: s.name,
      unlocked: report.unlocked,
    }))
  );
  // Deduplicate by ticker (chips may share companies)
  const seen = new Set();
  return all.filter((c) => {
    if (seen.has(c.ticker)) return false;
    seen.add(c.ticker);
    return true;
  });
};

export const getAllCompaniesAcrossReports = () =>
  REPORTS.flatMap((r) => getAllCompanies(r));

// ─── AUTO-UPDATE PRICES ──────────────────────────────────────────────────────
// Fetches /prices.json (generated daily by update-prices.js) and patches all
// company.now values in place so the dashboard always shows current prices.
export const updatePricesFromJSON = async () => {
  try {
    const resp = await fetch("/prices.json?t=" + Date.now());
    if (!resp.ok) return false;
    const prices = await resp.json();
    if (!prices || !prices.updated) return false;
    for (const report of REPORTS) {
      const allStages = report.chips
        ? report.chips.flatMap((c) => c.stages)
        : report.stages;
      for (const stage of allStages) {
        for (const company of stage.companies) {
          if (prices[company.ticker] != null) {
            company.now = prices[company.ticker];
          }
        }
      }
    }
    return prices.updated;
  } catch {
    return false;
  }
};

// ─── HISTORICAL PRICE DATA ────────────────────────────────────────────────────
// Fetches /historical.json (weekly price history for each ticker) and returns
// a map of ticker → [{date: Date, price: number}, ...] sorted chronologically.
let _historicalCache = null;
export const loadHistoricalPrices = async () => {
  if (_historicalCache) return _historicalCache;
  try {
    const resp = await fetch("/historical.json?t=" + Date.now());
    if (!resp.ok) return null;
    const raw = await resp.json();
    const parsed = {};
    for (const [key, val] of Object.entries(raw)) {
      if (key === "updated" || !Array.isArray(val)) continue;
      parsed[key] = val.map(([ts, price]) => ({
        date: new Date(ts * 1000),
        price,
      }));
    }
    _historicalCache = parsed;
    return parsed;
  } catch {
    return null;
  }
};

// Returns historical data for a specific ticker filtered to a date range.
// reportDate: string like "2025-09-13", rangeMonths: null = all, or 1/3/6
export const getTickerHistory = (historicalData, ticker, reportDateStr, rangeMonths = null) => {
  if (!historicalData || !historicalData[ticker]) return null;
  const all = historicalData[ticker];
  const reportDate = new Date(reportDateStr + "T00:00:00");

  // Find the first data point at or before report date for "pre-report" section
  let filtered;
  if (rangeMonths) {
    const cutoff = new Date();
    cutoff.setMonth(cutoff.getMonth() - rangeMonths);
    filtered = all.filter(p => p.date >= cutoff);
  } else {
    // Show from ~2 weeks before report date
    const preStart = new Date(reportDate);
    preStart.setDate(preStart.getDate() - 14);
    filtered = all.filter(p => p.date >= preStart);
  }

  if (filtered.length < 2) filtered = all;

  // Find the index of the data point closest to the report date
  let reportIdx = 0;
  let minDist = Infinity;
  for (let i = 0; i < filtered.length; i++) {
    const dist = Math.abs(filtered[i].date - reportDate);
    if (dist < minDist) { minDist = dist; reportIdx = i; }
  }

  return { points: filtered, reportIdx };
};

// ─── MERGE LOCKED DATA ─────────────────────────────────────────────────────
// After pro auth, the frontend fetches locked company data from the server
// and merges it back into the REPORTS array, replacing CLASSIFIED placeholders.
const mergeStage = (targetStage, stage) => {
  targetStage.companies = stage.companies;
  if (stage.name) targetStage.name = stage.name;
  if (stage.desc) targetStage.desc = stage.desc;
  if (stage.codename) targetStage.codename = stage.codename;
};
export const mergeLockedData = (lockedReports) => {
  for (const [reportId, data] of Object.entries(lockedReports)) {
    const report = REPORTS.find((r) => r.id === reportId);
    if (!report) continue;

    if (data.chips && report.chips) {
      for (const chip of data.chips) {
        const targetChip = report.chips.find((c) => c.id === chip.id);
        if (!targetChip) continue;
        for (const stage of chip.stages) {
          const targetStage = targetChip.stages.find((s) => s.id === stage.id);
          if (!targetStage) continue;
          mergeStage(targetStage, stage);
        }
      }
    } else if (data.stages && report.stages) {
      for (const stage of data.stages) {
        const targetStage = report.stages.find((s) => s.id === stage.id);
        if (!targetStage) continue;
        mergeStage(targetStage, stage);
      }
    }
  }
};
